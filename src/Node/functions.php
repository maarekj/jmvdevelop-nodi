/**
 * @param Node|Node[] ...$nodes
 */
function frag(...$nodes): FragmentNode
{
    $flattenNode = [];

    foreach ($nodes as $node) {
        if (\is_array($node)) {
            foreach ($node as $n) {
                $flattenNode[] = $n;
            }
        } else {
            $flattenNode[] = $node;
        }
    }

    return new FragmentNode($flattenNode);
}
